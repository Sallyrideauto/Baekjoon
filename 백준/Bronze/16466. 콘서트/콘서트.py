'''
이 문제는 간단하게 최적화할 수 있습니다. 
티켓 번호가 1부터 시작하므로, 
가장 작은 번호의 티켓을 찾기 위해 1부터 시작하여 이미 판매된 티켓 번호 목록에 없는 첫 번째 번호를 찾으면 됩니다.

최적화된 접근 방식은 sold_ticket 리스트를 집합(set)으로 변환하여 포함 여부를 확인하는 것입니다. 
집합은 원소의 포함 여부를 확인할 때 평균적으로 O(1)의 시간 복잡도를 가지므로 훨씬 효율적입니다.

이 코드는 1부터 시작하여 sold_ticket 집합에 포함되어 있지 않은 첫 번째 정수를 찾습니다. 
이 방법은 간단하고 효율적으로 문제를 해결합니다.
'''

N = int(input())
sold_ticket = set(map(int, input().split()))

i = 1
while i in sold_ticket:
    i += 1

print(i)
