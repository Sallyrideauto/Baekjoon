change = 1000 - int(input())  # 타로가 낸 돈과 물건값의 차이를 계산합니다.
coins = [500, 100, 50, 10, 5, 1]  # 잔돈의 종류를 리스트로 정의합니다.

count = 0  # 잔돈의 개수를 카운트하는 변수를 초기화합니다.
for coin in coins:
    count += change // coin  # 현재 잔돈의 종류로 거슬러 줄 수 있는 최대 개수를 계산합니다.
    change %= coin  # 거슬러 준 후에 남은 금액을 계산합니다.

print(count)  # 결과를 출력합니다.

