n = int(input())

# n이 0 또는 1일 경우, 각각 0과 1을 출력합니다.
if n <= 1:
    print(n)
else:
    # 초기값으로 0과 1을 설정합니다.
    a, b = 0, 1
    # n-1번 반복하여 피보나치 수를 계산합니다.
    for i in range(n-1):
        # a+b 값을 다음 피보나치 수로 업데이트합니다.
        a, b = b, a+b
    # n번째 피보나치 수를 출력합니다.
    print(b)

