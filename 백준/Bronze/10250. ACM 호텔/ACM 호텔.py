t = int(input()) # 테스트 케이스 개수 입력

for _ in range(t):
    h, w, n = map(int, input().split()) # 호텔의 층 수, 각 층의 방 수, 몇 번째 손님인지 입력
    floor = n % h # 층 수 계산
    room = n // h + 1 # 호 수 계산
    if floor == 0: # 예외 처리
        floor = h
        room -= 1
    print(floor * 100 + room) # 방 번호 출력

