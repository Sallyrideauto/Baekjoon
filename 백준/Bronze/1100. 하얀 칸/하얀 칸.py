count = 0  # 하얀 칸 위에 있는 말의 개수를 저장할 변수
for i in range(8):
    line = input()  # 체스판의 한 줄을 입력받음
    for j in range(8):
        if (i+j) % 2 == 0 and line[j] == 'F':  # 하얀 칸이면서 말이 있는 경우
            count += 1

print(count)

"""
위 코드에서는 count 변수를 초기화하고, 체스판의 각 칸에 대해 하얀 칸이면서 말이 있는 경우에만 count 변수를 1씩 증가시키도록 구현하였습니다. 
하얀 칸인지는 행과 열의 합이 짝수인지를 판단하여, 해당 칸이 하얀 칸인 경우에만 말이 있는지를 검사하였습니다.
"""