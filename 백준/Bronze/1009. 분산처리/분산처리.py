import sys

T = int(sys.stdin.readline())

for _ in range(T):
    a, b = map(int, sys.stdin.readline().split())
    remainder = (b % 4) + 4  # 4번 마다 패턴이 반복됨
    answer = pow(a, remainder, 10)  # pow(a, b, c)는 a^b % c를 계산함
    if answer == 0:
        print(10)
    else:
        print(answer)

