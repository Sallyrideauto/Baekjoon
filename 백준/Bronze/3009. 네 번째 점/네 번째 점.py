x = [0] * 1001
y = [0] * 1001

for i in range(3):
    xi, yi = map(int, input().split())
    x[xi] += 1
    y[yi] += 1

for i in range(1, 1001):
    if x[i] == 1:
        x4 = i
    if y[i] == 1:
        y4 = i

print(x4, y4)

