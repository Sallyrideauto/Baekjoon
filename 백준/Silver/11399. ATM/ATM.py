n = int(input())
p = list(map(int, input().split()))

p.sort()  # 오름차순으로 정렬
result = 0
for i in range(n):
    result += p[i] * (n - i)  # 현재 사람부터 마지막까지 걸리는 시간을 계산하여 누적

print(result)  # 결과 출력

